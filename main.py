async def show_progress(context: CallbackContext, chat_id: int, lang: str, duration: float = 0.5):
    processing_label = {"ru": "–û–±—Ä–∞–±–æ—Ç–∫–∞", "ua": "–û–±—Ä–æ–±–∫–∞", "en": "Processing"}
